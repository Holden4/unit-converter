<?php
// number in pounds we want to convert to kilograms
// floating point value for the pound to kilograms conversion

$pounds = 30;
$lb_to_kg = 0.453592;
$kilograms = $pounds * $lb_to_kg;

echo "Weight: ";
echo $pounds;
echo " lb = ";
echo $kilograms;
echo "kg | ";

// number in miles we want to convert to kilometers
// floating point value for the pound to Kilograms conversion

$miles = 10;
$miles_to_km = 1.60934;
$kilometers = $miles * $miles_to_km;


echo "Distance: ";
echo $miles;
echo " miles = ";
echo $kilometers;
echo "km";


?>
